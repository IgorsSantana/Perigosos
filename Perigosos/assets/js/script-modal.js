function abrirModalTuty(){
    const modal = document.getElementById('janela-modal-tuty')
    modal.classList.add('abrir-modal')

    modal.addEventListener('click', (e) => {
        if(e.target.id == 'fechar' || e.target.id == 'janela-modal-tuty') {
            modal.classList.remove('abrir-modal')
        }
    })
}
